    public void ListarDatos() throws ClassNotFoundException, SQLException {
        //Como el m√©todo de la BL retorna un ArrayList de la clase Estudiantes
        //Aqui lo recuperamos

        ArrayList<Estudiante> lstestudiante = blalumno.ConsultaLista();

        Object columnas[] = {"nombres", "apellidos", "edad", "cedula", "direccion", "Telefono"};
        DefaultTableModel modelo = new DefaultTableModel(null, columnas);
        jTable1.setModel(modelo);

        for (Estudiante objeto : lstestudiante) {
            //String id=String.valueOf(objeto.getStrCodigo());
            String nombres = objeto.getNombre();
            String apellidos = objeto.getApellido();
            String direccion = objeto.getDireccion();
            String cedula = objeto.getCedula();
            int edad = objeto.getEdad();
            String telefono = objeto.getTelefono();
            System.out.println(nombres + apellidos + edad + cedula + direccion + telefono);
            String modeloTemp[] = {nombres, apellidos, String.valueOf(edad), cedula, direccion, telefono};
            modelo.addRow(modeloTemp);
        }

    }

