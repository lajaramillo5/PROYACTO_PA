    public boolean validar() {
        if (jTextNombre.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "campo de texto del nombre del alumno esta vacio", "estudiante", JOptionPane.INFORMATION_MESSAGE);
            return false;
        } else if (jTextApellidp.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "campo de texto del apellido del alumno esta vacio", "estudiante", JOptionPane.INFORMATION_MESSAGE);
            return false;

        } else if (jTextCedula.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "campo de texto del documento del alumno esta vacio", "estudiante", JOptionPane.INFORMATION_MESSAGE);
            return false;
        } else if (jTextTelefono.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "campo de texto del telefono del alumno esta vacio", "estudiante", JOptionPane.INFORMATION_MESSAGE);
            return false;
        } else if (jTextDireccion.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "campo de texto del RH del alumno esta vacio", "estudiante", JOptionPane.INFORMATION_MESSAGE);
            return false;
        } else {
            try {
                Integer.parseInt(jTextCedula.getText());
                return true;
            } catch (NullPointerException | NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "algun dato esta con el formato incorrecto", "estudiante", JOptionPane.INFORMATION_MESSAGE);
                return false;
            }

        }
    }

    private void crearEstudiante() throws ClassNotFoundException, SQLException {
        boolean creado = false;
        if (validar()) {

            alumno.setCedula(String.valueOf(jTextCedula.getText()));
            alumno.setTelefono(String.valueOf(jTextTelefono.getText()));
            alumno.setNombre(jTextNombre.getText());
            alumno.setApellido(jTextApellidp.getText());
            alumno.setDireccion(jTextDireccion.getText());
            alumno.setEdad((int) this.jSpinner1.getValue());

            if (blalumno.Consultar(alumno)) {
                if (expre.calle(jTextDireccion.getText()) && expre.NumeroTelefono(jTextTelefono.getText())) {
                    creado = blalumno.RegistrarEstudiante(alumno);
                } else {
                    JOptionPane.showMessageDialog(this, "direccion o numero incorectos", "Crear estudiante", JOptionPane.INFORMATION_MESSAGE);
                    limpiar();
                }
                if (creado) {
                    limpiar();
                    this.ListarDatos();
                    JOptionPane.showMessageDialog(this, "Se creo el alumno", "Crear estudiante", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "No se pudo Crear el alumno", "crear estudiante", JOptionPane.WARNING_MESSAGE);
                }
            } else {
                jTextCedula.setText("");
                jTextCedula.requestFocus();
                JOptionPane.showMessageDialog(this, "El alumno ya existe", "Crear alumno", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }

    private void eliminarEstudiante() throws SQLException, ClassNotFoundException {

        String cedula = this.jTextedita.getText();
        alumno.setCedula(cedula);
        if (blalumno.Consultar(alumno)) {
            blalumno.Eliminar(alumno);
            JOptionPane.showMessageDialog(this, "se elimino el estudiante", "estudiantes", JOptionPane.INFORMATION_MESSAGE);
            habilitarComponentes();
            this.ListarDatos();
            limpiar();
        } else {
            JOptionPane.showMessageDialog(this, "estudiante no existe", "estudiantes", JOptionPane.INFORMATION_MESSAGE);
        }

    }

    private void modificarEstudiante() throws ClassNotFoundException, SQLException {

        String Direccion = this.jTexteditdirec.getText();
        String Cedula = this.jTextedita.getText();
        alumno.setCedula(Cedula);

        if (blalumno.Consultar(alumno)) {
            
            alumno.setDireccion(Direccion);
            blalumno.Modificar(alumno);
            JOptionPane.showMessageDialog(this, "Direccion Modificada", "estudiantes", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "estudiante no existe", "estudiantes", JOptionPane.INFORMATION_MESSAGE);
        }

    }
