    public void Listar() throws ClassNotFoundException, SQLException{
         estu.setCedula(this.jcedula.getText());
        try {
            x = BLalumno.Consultar_id(estu);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(GUINotasEstudiantes.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(GUINotasEstudiantes.class.getName()).log(Level.SEVERE, null, ex);
        }
        estu.setCodigoCarnet(x);
              this.jTable1.removeAll();
       
            lista = BLnotas.Consultares(estu);
       
        // a√±ade los objetos a la tabla
        this.jTable1.setModel(modelo);
        for (Nota Objestu : lista) {
            String NewValor[] = {
                String.valueOf(Objestu.getAsignaturas().getIdasig()),
                String.valueOf(Objestu.getPar11()),
                String.valueOf(Objestu.getPar12()),
                String.valueOf(Objestu.getPar13()),
                String.valueOf(Objestu.getQuimestre1()),
                String.valueOf(Objestu.getPar21()),
                String.valueOf(Objestu.getPar22()),
                String.valueOf(Objestu.getPar23()),
                String.valueOf(Objestu.getQuimestre2()),
                String.valueOf(Objestu.getNotaFinal()),
            };
            modelo.addRow(NewValor);
        };
        

       
    }
