    public void ListarDatos() throws ClassNotFoundException, SQLException {
        //Como el método de la BL retorna un ArrayList de la clase Estudiantes
        //Aqui lo recuperamos

        ArrayList<Asignatura> lstdocentes = blasig.ConsultaLista();

        Object columnas[] = {"Asignatura", "Horario", "Grado", "Docente"};
        DefaultTableModel modelo = new DefaultTableModel(null, columnas);
        jTable1.setModel(modelo);

        for (Asignatura objeto : lstdocentes) {
            //String id=String.valueOf(objeto.getStrCodigo());
            String nombres = objeto.getNombre();
            String horario = objeto.getHorario();
            int docente = objeto.getDocente().getCodigoCarnet();
            int grado = objeto.getGrado().getId();
            String modeloTemp[] = {nombres, horario, String.valueOf(docente), String.valueOf(grado)};
            modelo.addRow(modeloTemp);
        }

    }

